#define _CRT_SECURE_NO_WARNINGS
#include<stdio.h>
#include<stdlib.h>
int fib(int n, int* F) {
	if (n == 1 || n == 2)
		return 1;

	if (F[n - 1] == 0) // memo에서 f(n-1)확인
		F[n - 1] = fib(n - 1, F); // memo에 없으면 호출
	if (F[n - 2] == 0) // memo에서 확인
		F[n - 2] = fib(n - 2, F); // 없으면 호출 -> 호출 횟수를 줄여줌

	return F[n - 1] + F[n - 2];
}
int main(void) {

	int n, i;
	int* F;

	printf("몇 번째까지의 피보나치 수열:");
	scanf("%d", &n);

	// memo 초기화
	F = (int*)malloc(sizeof(int*) * (n + 1));
	for (i = 1; i <= n; i++)
		F[i] = 0;

	for (i = 1; i <= n; i++)
		printf("%d ", fib(i, F));


	free(F);
	return 0;
}