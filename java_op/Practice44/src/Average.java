public class Average {
	
	public double getAverage(int a, int b) {return ((double)(a + b) / 2);}
	public double getAverage(int a, int b, int c){return ((double)(a + b + c) / 3);}
	public double getAverage(double a, double b) {return ((double)(a + b) / 2);}
	public double getAverage(double a, double b, double c){return ((double)(a + b + c) / 3);}
	
}
