public class Person {

	private String address, name;
    private int age;

    public void setName(String name) {
    	this.name = name;
    	}
    public String getName() {
		return name;
		}
    
    public void setAddress(String address) {
    	this.address = address;
    	}
	public String getAddress(){
		return address;
		}
	
	public void setAge (int age){
		this.age = age;
		}
	public int getAge(){
		return age;
	}
} 

